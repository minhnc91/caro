package caro;

public class NextMove {
    int val;
    int x;
    int y;

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public NextMove(int val, int x, int y) {
        this.val = val;
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
